use std::fs::File;
use std::io::Write;
use std::path::Path;

use crate::Result;

pub fn is_first_run(data_path: &Path) -> Result<bool> {
    let path = data_path.join("glean_first_run.txt");

    if path.exists() {
        return Ok(false);
    }

    let mut file = File::create(path)?;
    file.write_all(b"# DO NOT TOUCH - AUTO GENERATED BY GLEAN")?;

    Ok(true)
}
